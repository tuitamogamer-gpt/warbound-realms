// Consumables that only do something during combat (can't be used on the map).
export const isCombatOnlyConsumable = (id) => {
  const fx = ITEMS[id]?.effects || {}
  return !!(fx.combatDice || fx.combatAutoHits || fx.combatArmor)
}
